package com.javainuse.model;

public class Marks {
	String id, name, email, busId;
	Integer  contact, tickets;
	public String getId() {
		return id;
	}
	public void setId(String i) {
		id = i;
	}
	public String getName() {
		return name;
	}
	public void setName(String grade) {
		name = grade;
	}
	public Integer getContact() {
		return contact;
	}
	public void setContact(Integer integer) {
		contact = integer;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String s) {
		email = s;
	}
	public String getBusId() {
		return busId;
	}
	public void setBusId(String s) {
		busId = s;
	}
	public Integer getTickets() {
		return tickets;
	}
	public void setTickets(int s) {
		tickets = s;
	}

}
